# Grocery Store Billing System

items = []
total = 0.0

print("Enter items (type 'done' to finish)\n")

while True:
    name = input("Item name: ")
    if name.lower() == "done":
        break

    qty = int(input("Quantity: "))
    price = float(input("Price per item ($): "))

    cost = qty * price
    total += cost
    items.append((name, qty, price, cost))
    print()

# Discount logic
discount = 0
if total > 100:
    discount = 0.10
elif total > 50:
    discount = 0.05

discount_amount = total * discount
final_total = total - discount_amount

# Receipt printing
print("\n" + "=" * 45)
print("         GROCERY STORE RECEIPT")
print("=" * 45)
print(f"{'Item':15}{'Qty':>5}{'Price':>10}{'Total':>10}")
print("-" * 45)

for item in items:
    print(f"{item[0]:15}{item[1]:>5}{item[2]:>10.2f}{item[3]:>10.2f}")

print("-" * 45)
print(f"{'Sub Total':30}${total:>10.2f}")
print(f"{'Discount':30}-${discount_amount:>9.2f}")
print("=" * 45)
print(f"{'Final Amount':30}${final_total:>10.2f}")
print("=" * 45)

