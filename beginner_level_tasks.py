# Mobile Balance & Package Simulator

current_balance = 10  # starting balance

while True:
    print("\n--- Mobile Menu ---")
    print("1. Check Balance")
    print("2. Recharge")
    print("3. Subscribe Bundle ($5)")
    print("4. Exit")

    choice = input("Choose an option: ")

    if choice == "1":
        print(f"Your current balance is: ${current_balance}")

    elif choice == "2":
        amount = float(input("Enter recharge amount: "))
        if amount > 0:
            current_balance += amount
            print(f"Recharged successfully. New balance: ${current_balance}")
        else:
            print("Invalid recharge amount")

    elif choice == "3":
        package_price = 5
        if current_balance >= package_price:
            current_balance -= package_price
            print("Bundle subscribed successfully!")
            print(f"Remaining balance: ${current_balance}")
        else:
            print("Insufficient balance to subscribe this bundle")

    elif choice == "4":
        print("Exiting program. Goodbye!")
        break

    else:
        print("Invalid option. Try again.")